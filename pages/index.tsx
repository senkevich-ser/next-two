import Head from "next/head";
import HTag from "@/components/HTag/HTag";
import Button from "@/components/Button/Button";
import Paragraph from "@/components/Paragraph/Paragraph";
import Tag from "@/components/Tag/Tag";
import { useState } from "react";
import Rating from "@/components/Rating/Rating";
import { withLayout } from "@/components/Layout/Layout";
import axios from "axios";
import { GetStaticProps } from "next";
import { MenuItem } from "../interfaces/menu.interface";
import Input from "@/components/Input/Input";

function Home({ menu, firstCategory }: HomeProps) {
  const [directionPrimary, setDirectionPrimary] = useState<boolean>(true);
  const [directionGhost, setDirectionGhost] = useState<boolean>(true);
  const [rating, setRating] = useState<number>(4);

  return (
    <>
      <Head>
        <title>Next-Two</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <>
        <HTag tag="h1">Big text</HTag>
        <HTag tag="h2">Big text</HTag>
        <HTag tag="h3">Big text</HTag>
        <Button>Кнопка</Button>
        <Button
          arrow={directionPrimary ? "right" : "down"}
          appearance={"primary"}
          onClick={() => setDirectionPrimary(!directionPrimary)}
        >
          Кнопка
        </Button>
        <Button
          arrow={directionGhost ? "right" : "down"}
          appearance={"ghost"}
          onClick={() => setDirectionGhost(!directionGhost)}
        >
          Кнопка
        </Button>
        <Paragraph size={"small"}>Small paragraph</Paragraph>
        <Paragraph size={"medium"}>Medium paragraph</Paragraph>
        <Paragraph size={"large"}>Large paragraph</Paragraph>
        <Tag size={"small"} color={"ghost"}>
          Small Ghost
        </Tag>
        <Tag size={"small"} color={"primary"}>
          Small Primary
        </Tag>
        <Tag size={"large"} color={"red"}>
          Large Red
        </Tag>
        <Tag size={"large"} color={"grey"}>
          Large Grey
        </Tag>
        <Rating rating={rating} isEditable={true} setRating={setRating} />
        <Input placeholder="Test"/>
      </>
    </>
  );
}
export default withLayout(Home);

export const getStaticProps: GetStaticProps = async () => {
  const firstCategory = 0;
  const { data: menu } = await axios.post<MenuItem[]>(
    process.env.NEXT_PUBLIC_DOMAIN + "/api/top-page/find",
    {
      firstCategory,
    }
  );
  return {
    props: {
      menu,
      firstCategory,
    },
  };
};

interface HomeProps extends Record<string, unknown> {
  menu: MenuItem[];
  firstCategory: number;
}
